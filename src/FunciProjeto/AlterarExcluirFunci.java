    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeFunci.getText();
        String telefone = txtTelefone.getText();
        String status = txtStatusFunci.getText();
        String idProjeto = txtIDProjeto.getText();
        int id = Integer.parseInt(idProjeto);
        String idFunci1 = txtIDFunci1.getText();
        int id2 = Integer.parseInt(idFunci1);
        String departamento = txtDepartamento.getText();
        String funcao = txtFuncaoFunci.getText();

        FunciProjeto f = new FunciProjeto();
        f.setNomeFuncionario(nome);
        f.setTelefoneFuncionario(telefone);
        f.setStatusFuncionario(status);
        f.setIDProjeto(id);
        f.setDepartamento(departamento);
        f.setFuncao(funcao);
        f.setIDFuncionario(id2);
        try {
            f.AlterarFunci();
            acaobotaolimpa();
            JOptionPane.showMessageDialog(null, "Funcionário " + nome + " alterado com sucesso!!");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Por favor digite as informações corretamente");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

